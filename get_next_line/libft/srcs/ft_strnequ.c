#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (!s1 || !s2 || n == 0)
		return (1);
	else
		while (n > 0 && *s1 && *s2)
		{
			if (*s1 != *s2)
				break ;
			n--;
			s1++;
			s2++;
		}
	if (n == 0 || *s1 == *s2)
		return (1);
	return (0);
}
